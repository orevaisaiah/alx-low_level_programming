#include <stdio.h>

/**
 * add - add two ints
 * @a: first int to add
 * @b: second int to add
 * Return: sum of both ints
 */

int add(int a, int b)
{
	int result;

	result = a + b;
	return (result);
}


/**
 * sub - subtract an ints from another
 * @a: first int
 * @b: second int
 * Return: the result of the int subtraction
 */


int sub(int a, int b)
{
	int result;

	result = a - b;
	return (result);

}


/**
 * mul - multiply two ints
 * @a: first int
 * @b: second int
 * Return: the result of the int multiplication
 */

int mul(int a, int b)
{
	int result;

	result = a * b;
	return (result);

}


/**
 * div - carry out division between two int
 * @a: first int
 * @b: second int
 * Return: the result of the ints division
 */



int div(int a, int b)
{
	int result;

	result = a / b;
	return (result);

}


/**
 * mod - find the modulos of two ints
 * @a: first int
 * @b: second int
 * Return: the result of the modulus of two int
 */

int mod(int a, int b)
{
	int result;

	result = a % b;
	return (result);

}

